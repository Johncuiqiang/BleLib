    /**
     * 计算数组的CRC8校验值
     *
     * @param data 需要计算的数组
     * @return CRC8校验值
     */
    public static byte calcCrc8(byte[] data) {
        return calcCrc8(data, 0, data.length, (byte) 0);
    }

    /**
     * 计算CRC8校验值
     *
     * @param data   数据
     * @param offset 起始位置
     * @param len    长度
     * @return 校验值
     */
    public static byte calcCrc8(byte[] data, int offset, int len) {
        return calcCrc8(data, offset, len, (byte) 0);
    }

    /**
     * 计算CRC8校验值
     *
     * @param data   数据
     * @param offset 起始位置
     * @param len    长度
     * @param preval 之前的校验值
     * @return 校验值
     */
    public static byte calcCrc8(byte[] data, int offset, int len, byte preval) {
        byte ret = preval;
        for (int i = offset; i < (offset + len); ++i) {
            int datas = data[i] & 0xFF;
            //^异或
            ret = crc8_tab[(0x00ff & (ret ^ datas))];
        }
        return ret;
    }

    /**
     *  有符号转无符号
     */
    public static int getUnsignedByte(byte data) {      //将data字节型数据转换为0~255 (0xFF 即BYTE)。
        return data & 0xFF;
    }

    /**
     * 计算CRC8校验值,异或非查表法
     */
    public static byte crc8(final byte[] input, final int len){
        byte crc = 0x00;
        for (int i = 0; i< len; i ++){
            crc ^=input[i];
        }
        return crc;
    }

